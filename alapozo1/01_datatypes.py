# boolean
b1, b2 = True, False # case sensitive!!!
# integer
i1, i2, i3 = 3, 56, 777777
# float
f1, f2 = 3.14, 67.89745678
# string
str1 = 'ddddd'
str2 = "fggfg"
str3 = """dert"""
str4 = "It's my job." # always use ""
str5 = "It\'s my job." # escape char
# list
list1 = [1, 3, "ert", True, 3.24, 678, str1, [b1, b2]]
# set - only unique items are allowed
set1 = {1, 2, 3, 3, 2}
list1.append(set1)
print("List: ", list1)
print("Set: ", set1)
print("String: ", str1, str2, str3, str4, str5)
